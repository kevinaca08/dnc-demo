/* 以學生 id 為種子，產生穩定（不會每次重整就變）的近期完成任務。
   type = 'short' | 'long'。回傳 [{name, exp, time}, …]，已依時間新→舊排序。
   任務名以「不放回」方式抽取，同一份清單不會重複。 */
window.CLASS_DATA.recentQuests = function (id, type) {
  function seeded(a) {
    return function () {
      a |= 0; a = (a + 0x6D2B79F5) | 0;
      var t = Math.imul(a ^ (a >>> 15), 1 | a);
      t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
      return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
    };
  }
  var pool = window.CLASS_DATA.questPool[type].slice();  // 複製一份來洗牌
  var rnd = seeded(id * 7919 + (type === "short" ? 1 : 2));
  // Fisher–Yates 洗牌（種子化）→ 取前 n 個＝不重複
  for (var k = pool.length - 1; k > 0; k--) {
    var j = Math.floor(rnd() * (k + 1));
    var tmp = pool[k]; pool[k] = pool[j]; pool[j] = tmp;
  }
  var want = type === "short" ? 6 + Math.floor(rnd() * 7)   // 短期 6–12 筆
                              : 3 + Math.floor(rnd() * 4);  // 長期 3–6 筆
  var n = Math.min(want, pool.length);                       // 不超過池大小，確保不重複
  var out = [];
  for (var i = 0; i < n; i++) {
    var exp = type === "short" ? 10 + Math.floor(rnd() * 21)   // 短期 10–30
                               : 40 + Math.floor(rnd() * 41);  // 長期 40–80（< 一級 100）
    var d  = 5 + Math.floor(rnd() * 30);           // 5/05 ~ 6/03 區間
    var mo = d > 31 ? 6 : 5, dd = d > 31 ? d - 31 : d;
    var hh = 8 + Math.floor(rnd() * 9), mm = Math.floor(rnd() * 60);
    var pad = function (x) { return x < 10 ? "0" + x : "" + x; };
    out.push({ name: pool[i], exp: exp, time: mo + "/" + pad(dd) + " " + pad(hh) + ":" + pad(mm) });
  }
  out.sort(function (a, b) { return a.time < b.time ? 1 : -1; });  // 新→舊
  return out;
};
